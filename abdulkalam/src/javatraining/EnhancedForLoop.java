package javatraining;

public class EnhancedForLoop {

	public static void main(String[] args)
	{
		String[] x=new String[6];
		x[0]="Madhan";
		x[1]="Swathi";
		x[3]="Vijaya";
		x[2]="Manohar";
		x[5]="Appu";
		x[4]="Johnny";
		
		
		for(String i : x)
		{
			System.out.println(i);
			
		}
		

	}

}
